fun main() {
    //Tipe system dinamis tidak menentukan tipe secara otomatis berdasarkan nilainya.
    var dynamicVar = 2
    println("Contoh dynamic variabel = " + dynamicVar)

    //tipe system  statis memerlukan deklarasi jenis tipe data,
    // dan data tersebut tidak bisa di rubah selama program di eksekusi
